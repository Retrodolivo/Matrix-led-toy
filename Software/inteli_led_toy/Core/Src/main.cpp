Game_snake *snake;
Game_food *food;

uint8_t snake_speed = Game_snake::SPEED_RATE::THREE;


void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
    if (htim->Instance == snake->fps_timer->Instance)
    {
    	switch (snake->get_obj_ahead(food))
    	{
			case FOOD:
				snake->make_step();
				snake->devour(food);
				food = new Game_food(&ws2812);
				break;
			case OBSTACLE:
				game_over();
				break;
			case NONE:
				snake->make_step();
				break;
		}
    }
    if (htim->Instance == joystick.polling_timer->Instance)
    {
    	if (joystick.rot_val[0] < joystick.rot_lim.x_low)
    		snake->turn(Game_snake::UP);
    	if (joystick.rot_val[0] > joystick.rot_lim.x_high)
    		snake->turn(Game_snake::DOWN);
    	if (joystick.rot_val[1] < joystick.rot_lim.y_low)
    		snake->turn(Game_snake::RIGHT);
    	if (joystick.rot_val[1] > joystick.rot_lim.y_high)
    		snake->turn(Game_snake::LEFT);
    }
}

void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
	/*make snake move upon joystick button press*/
	if (GPIO_Pin == JS_BTN_Pin && !snake->is_moving)
	{
		if (snake->is_crush)
		{
			/*Clear field*/
			ws2812.fill_rect(0, 0, ws2812.Cols - 1, ws2812.Rows - 1, 0);
			snake = new Game_snake(&htim3, &ws2812, 3, RGB(8, 0, 0), RGB(0, 8, 0));
			snake->is_crush = false;
			snake->sr = static_cast<Game_snake::SPEED_RATE>(snake_speed);
		}
		food = new Game_food(&ws2812);
		snake->start_move();
	}
	if (GPIO_Pin == BTN_SPEED_Pin)
	{
//		snake_speed = snake->sr;
		if (snake->sr != Game_snake::SPEED_RATE::SIX)
		{
			snake->sr = static_cast<Game_snake::SPEED_RATE>(++snake_speed);
			snake_speed = snake->sr;
		}
		else
		{
			snake->sr = Game_snake::SPEED_RATE::ONE;
			snake_speed = snake->sr;
		}
		snake->set_speed(snake->sr);
	}
}

void game_over()
{
	snake->is_crush = true;
	snake->stop_move();
	snake->change_color(RGB(8, 0, 0));
	delete food;
	delete snake;
}

uint32_t RGB(uint8_t r, uint8_t g, uint8_t b)
{
	return r << 16 | g << 8 | b;
}
